#include "stdafx.h"
#include <Boxes/Box.h>

using namespace Boxes;


//==========================================================================================================================
//
//Constructors
//
//==========================================================================================================================
Box::Box(void) 
: 
_speed(200.0f),
_direction(0.0f)
{ 
    Init();
}

//==========================================================================================================================
//
//Virtual functions
//
//==========================================================================================================================
//==========================================================================================================================
//Update
//==========================================================================================================================
void Box::v_Update(void) 
{
    AddPosition(_direction * TM::Timer::Instance()->DeltaTime() * _speed);
    DefaultUpdate();
}

void Box::OnCollide(void)
{
    AddPosition(-_direction * TM::Timer::Instance()->DeltaTime() * _speed);
    _direction = 0.0f;
}